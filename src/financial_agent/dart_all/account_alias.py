# 빠른 역방향 조회용 캐시: variant → canonical
_REVERSE_ALIAS: dict[str, str] = {
    variant: canonical
    for canonical, variants in DART_ACCOUNT_ALIAS.items()
    for variant in variants
}


def normalize_account_name(raw: str) -> str:
    """
    DART 응답 계정명을 표준 한국어로 정규화.

    정규화 규칙:
        1. 좌우 공백 제거 + 연속 공백 1개로 축소
        2. DART_ACCOUNT_ALIAS 의 variant 와 매칭되면 canonical 반환
        3. 매칭 없으면 정리된 원문 그대로 반환 (DB 저장 후 사후 분석 가능)

    :param raw: DART API 가 반환한 raw account_nm
    :return: 정규화된 canonical 계정명 (매칭 없으면 cleaned raw)
    """
    if raw is None:
        return ""
    cleaned = " ".join(str(raw).split())  # 다중 공백 → 1개
    return _REVERSE_ALIAS.get(cleaned, cleaned)


def is_known_account(account_nm: str) -> bool:
    """정규화 후 사전에 등록된 표준 계정인지 여부."""
    canonical = normalize_account_name(account_nm)
    return canonical in DART_ACCOUNT_ALIAS
